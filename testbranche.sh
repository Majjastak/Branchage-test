Je suis une branche bien touffue de feuilles de séquoya. Le séquoya n'a pas de fruit mais son bois 
est très nourris. Or les chenilles me dérangent beacoup et bouffent sans arrêt mes feuilles
 ces spèces de gnous !
En fin de compte, mon jardinier m'a vangé, il les a toutes gazés ses enculées. Vive le séquoya. Vive la république et vive la France.
